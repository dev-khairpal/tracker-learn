<h2 class="mb-md text-headline-lg tracking-tight text-on-surface">Resources &amp; Tips</h2>

<div class="mb-lg rounded-lg border border-outline-variant bg-surface-container p-lg">
	<h2 class="text-headline-sm text-on-surface">The 7-step depth checklist</h2>
	<p class="mt-xs mb-sm text-body-md text-on-surface-variant">What "understanding" means here.</p>
	<ul class="list-disc space-y-xs pl-lg text-body-md text-on-surface">
		<li>
			<b>Intuition</b> — you can explain the core idea out loud in under 60 seconds, without notes.
		</li>
		<li>
			<b>Brute Force</b> — you thought through (or coded) the naive approach and its complexity first.
		</li>
		<li>
			<b>Optimized</b> — you implemented the optimized solution yourself, without reading the editorial
			line-by-line.
		</li>
		<li><b>Complexity</b> — you can state and justify time and space complexity.</li>
		<li>
			<b>Edge Cases</b> — you listed and handled edge cases (empty input, single element, duplicates,
			overflow, etc).
		</li>
		<li><b>Mistakes</b> — you noted a mistake you made or a common trap for this problem.</li>
		<li><b>Variations</b> — you know at least one realistic follow-up an interviewer might ask.</li>
	</ul>
	<p class="mt-sm text-body-md text-on-surface-variant">
		A problem becomes <b class="text-on-surface">Solved</b> only when all 7 are checked. It becomes
		<b class="text-on-surface">Mastered</b> only after passing spaced revision at +1 day, +7 days and
		+30 days — see the Planner page's Revision Center.
	</p>
</div>

