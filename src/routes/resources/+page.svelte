	<ul class="list-disc space-y-xs pl-lg text-body-md text-on-surface">
		<li><b>Competitive Programming</b> → Contest speed, advanced math, obscure algorithms.</li>
		<li>
			<b>Interview DSA</b> → Problem-solving, clean code, communication, common algorithmic patterns.
		</li>
		<li>
